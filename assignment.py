#BITWISE operator
# & operator
a=50   #110010
b=25   #011001
c=a & b
print(c)

# | operator
a=50   #110010
b=25   #011001
c=a | b
print(c)

# ^ operator
a=50   #110010
b=25   #011001
c=a ^ b
print(c)

# << operator
a=240     #11110000
b=a<<2    #11000011

print(b)

# >> operator
a=240     #11110000
b=a>>2    #00111100

print(b)
